using System;
using System.Linq;
using Content.Client._Sunrise.Sheetlets.SciFiStyle;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Maths;
using Robust.Shared.Timing;

namespace Content.Client._Sunrise.Sponsor;

[GenerateTypedNameReferences]
public sealed partial class SponsorStorePackCard : ContainerButton
{
    private const float PreviewCycleInterval = 1.8f;
    private const int NameMaxLineLength = 14;
    private const int NameMaxLines = 2;

    private readonly string[] _cyclePrototypes;
    private float _cycleTimer;
    private int _cycleIndex;

    public event Action? OnSelected;
    public event Action? OnBuyPressed;

    public SponsorStorePackCard(
        string? previewPrototype,
        string[] itemPrototypes,
        string name,
        int price,
        string buyText,
        bool buyDisabled,
        bool selected,
        bool owned,
        int ownedCount,
        int totalCount)
    {
        RobustXamlLoader.Load(this);

        StyleBoxOverride = CreatePanelStyle(selected, owned);

        _cyclePrototypes = string.IsNullOrWhiteSpace(previewPrototype)
            ? itemPrototypes.ToArray()
            : [];
        PackPreview.SetPrototype(string.IsNullOrWhiteSpace(previewPrototype)
            ? GetCyclePrototype()
            : previewPrototype);

        PackName.Text = SponsorUiHelpers.WrapText(name, NameMaxLineLength, NameMaxLines);

        PackProgress.Text = Loc.GetString(
            "donation-terminal-inventory-pack-progress",
            ("owned", ownedCount),
            ("total", totalCount));

        PackPrice.Text = Math.Max(price, 0).ToString();

        BuyButton.Text = buyText;
        BuyButton.Disabled = buyDisabled;
        BuyButton.OnPressed += _ => OnBuyPressed?.Invoke();

        OnPressed += _ => OnSelected?.Invoke();
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        if (_cyclePrototypes.Length <= 1)
            return;

        _cycleTimer += args.DeltaSeconds;
        if (_cycleTimer < PreviewCycleInterval)
            return;

        _cycleTimer %= PreviewCycleInterval;
        _cycleIndex = (_cycleIndex + 1) % _cyclePrototypes.Length;
        PackPreview.SetPrototype(GetCyclePrototype());
    }

    private string? GetCyclePrototype()
    {
        if (_cyclePrototypes.Length == 0)
            return null;

        return _cyclePrototypes[Math.Clamp(_cycleIndex, 0, _cyclePrototypes.Length - 1)];
    }

    private static StyleBoxFlat CreatePanelStyle(bool selected, bool owned)
    {
        var border = selected
            ? SciFiPalette.Accent
            : owned
                ? SciFiPalette.Accent
                : SciFiPalette.BorderDim;

        var style = new StyleBoxFlat
        {
            BackgroundColor = owned ? SciFiPalette.AccentSoft : SciFiPalette.PanelBackgroundDark.WithAlpha(0.6f),
            BorderColor = border.WithAlpha(0.88f),
            BorderThickness = new Thickness(1),
        };
        return style;
    }
}
