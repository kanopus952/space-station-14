using System;
using Content.Client._Sunrise.Sheetlets.SciFiStyle;
using Content.Shared._Sunrise.Helpers;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Maths;

namespace Content.Client._Sunrise.Sponsor;

[GenerateTypedNameReferences]
public sealed partial class SponsorStoreItemCard : ContainerButton
{
    private const int NameMaxLineLength = 14;
    private const int NameMaxLines = 3;

    public event Action? OnSelected;
    public event Action? OnBuyPressed;

    public SponsorStoreItemCard(
        string previewPrototype,
        string name,
        int price,
        string buyText,
        bool buyDisabled,
        bool selected,
        bool owned)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        StyleBoxOverride = CreatePanelStyle(selected, owned);

        ItemPreview.SetPrototype(previewPrototype);
        ItemName.Text = name.WrapText(NameMaxLineLength, NameMaxLines);

        ItemPrice.Text = Math.Max(price, 0).ToString();

        BuyButton.Text = buyText;
        BuyButton.Disabled = buyDisabled;
        BuyButton.OnPressed += _ => OnBuyPressed?.Invoke();

        OnPressed += _ => OnSelected?.Invoke();
    }

    private static StyleBoxFlat CreatePanelStyle(bool selected, bool owned)
    {
        var border = selected || owned
            ? SciFiPalette.Accent
            : SciFiPalette.BorderDim;

        var style = new StyleBoxFlat
        {
            BackgroundColor = owned ? SciFiPalette.AccentSoft : SciFiPalette.PanelBackgroundDark.WithAlpha(0.6f),
            BorderColor = border.WithAlpha(0.88f),
            BorderThickness = new Thickness(1),
        };
        return style;
    }
}
