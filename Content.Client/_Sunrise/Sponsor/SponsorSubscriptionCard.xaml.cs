using System;
using System.Collections.Generic;
using Content.Client._Sunrise.Sheetlets.SciFiStyle;
using Content.Client.Resources;
using Content.Client.Stylesheets;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Maths;
using Robust.Shared.Utility;

namespace Content.Client._Sunrise.Sponsor;

[GenerateTypedNameReferences]
public sealed partial class SponsorSubscriptionCard : BoxContainer
{
    private const int SubscriptionBenefitTextWidth = 260;
    private const int SubscriptionBenefitLineLength = 34;
    private const int SubscriptionTierBadgeFontSize = 24;
    private const int BenefitMarkerFontSize = 14;

    [Dependency] private readonly IResourceCache _resourceCache = default!;

    private Font? _subscriptionTierBadgeFont;
    private Font? _benefitMarkerFont;

    public event Action? DetailsPressed;

    public SponsorSubscriptionCard(
        string title,
        int tier,
        IReadOnlyList<string> benefits,
        Color accent,
        bool current)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        ToolTip = BuildTooltip(title, tier, benefits, current);
        TierBadgeLabel.Text = SponsorUiHelpers.ToRomanNumeral(tier);
        TierBadgeLabel.FontOverride = GetSubscriptionTierBadgeFont();
        TierBadgeLabel.FontColorOverride = accent;
        TitleLabel.Text = SponsorUiHelpers.WrapText(title, 25, 3);
        TitleLabel.ToolTip = title;
        TitleLabel.FontColorOverride = accent;

        DetailsButton.TextAlign = Label.AlignMode.Center;
        DetailsButton.OnPressed += _ => DetailsPressed?.Invoke();

        PopulateBenefits(benefits, accent);
    }

    private void PopulateBenefits(IReadOnlyList<string> benefits, Color accent)
    {
        BenefitsList.RemoveAllChildren();

        if (benefits.Count == 0)
        {
            AddBenefitRow(
                Loc.GetString("donation-terminal-subscription-card-no-benefits"),
                SciFiPalette.TextMuted,
                SciFiPalette.TextMuted);
            return;
        }

        foreach (var benefit in benefits)
        {
            AddBenefitRow(benefit, SciFiPalette.Text, accent);
        }
    }

    private void AddBenefitRow(string text, Color textColor, Color checkColor)
    {
        var row = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            SeparationOverride = 6,
            HorizontalExpand = true,
        };

        row.AddChild(new Label
        {
            Text = "\u2713",
            MinWidth = 18,
            Align = Label.AlignMode.Center,
            FontOverride = GetBenefitMarkerFont(),
            FontColorOverride = checkColor,
            StyleClasses = { StyleClass.LabelSubText },
            VAlign = Label.VAlignMode.Top,
        });

        var benefitLabel = new RichTextLabel
        {
            MaxWidth = SubscriptionBenefitTextWidth,
            HorizontalExpand = true,
            StyleClasses = { StyleClass.LabelSubText },
        };
        benefitLabel.SetMessage(FormattedMessage.FromUnformatted(text), textColor);

        row.AddChild(benefitLabel);
        BenefitsList.AddChild(row);
    }

    private string BuildTooltip(
        string title,
        int tier,
        IReadOnlyList<string> benefits,
        bool current)
    {
        var lines = new List<string>
        {
            title,
            Loc.GetString("donation-terminal-subscription-card-tier", ("tier", tier)),
        };

        if (current)
            lines.Add(Loc.GetString("donation-terminal-subscription-card-current"));

        lines.Add(string.Empty);
        lines.Add(BuildBenefitsText(benefits));
        return string.Join('\n', lines);
    }

    private string BuildBenefitsText(IReadOnlyList<string> benefits)
    {
        if (benefits.Count == 0)
            return Loc.GetString("donation-terminal-subscription-card-no-benefits");

        var lines = new List<string>();
        foreach (var benefit in benefits)
        {
            var wrappedLines = SponsorUiHelpers.WrapLines(benefit, SubscriptionBenefitLineLength);
            for (var i = 0; i < wrappedLines.Count; i++)
            {
                lines.Add(i == 0
                    ? "- " + wrappedLines[i]
                    : "  " + wrappedLines[i]);
            }
        }

        return string.Join('\n', lines);
    }

    private Font GetSubscriptionTierBadgeFont()
    {
        return _subscriptionTierBadgeFont ??= _resourceCache.GetFont(
            [
                "/Fonts/NotoSans/NotoSans-Bold.ttf",
                "/Fonts/NotoSans/NotoSansSymbols-Regular.ttf",
                "/Fonts/NotoSans/NotoSansSymbols2-Regular.ttf",
            ],
            SubscriptionTierBadgeFontSize);
    }

    private Font GetBenefitMarkerFont()
    {
        return _benefitMarkerFont ??= _resourceCache.GetFont(
            [
                "/Fonts/NotoSans/NotoSans-Regular.ttf",
                "/Fonts/NotoSans/NotoSansSymbols-Regular.ttf",
                "/Fonts/NotoSans/NotoSansSymbols2-Regular.ttf",
            ],
            BenefitMarkerFontSize);
    }
}
