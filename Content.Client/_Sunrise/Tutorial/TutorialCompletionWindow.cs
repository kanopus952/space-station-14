using System;
using System.Collections.Generic;
using Content.Client.Message;
using Content.Shared._Sunrise.Tutorial.Eui;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;

namespace Content.Client._Sunrise.Tutorial;

[GenerateTypedNameReferences]
public sealed partial class TutorialCompletionWindow : DefaultWindow
{
    public event Action<string>? ActionPressed;

    public TutorialCompletionWindow()
    {
        RobustXamlLoader.Load(this);
    }

    public void ApplyState(TutorialCompletionEuiState state)
    {
        var title = Loc.GetString(state.Title);

        Title = title;
        CompletionTitleLabel.Text = title;
        CompletionDescriptionLabel.SetMarkup(Loc.GetString(state.Description));

        RebuildButtons(state.Actions);
    }

    private void RebuildButtons(IEnumerable<TutorialCompletionEuiAction> actions)
    {
        CompletionActionButtons.DisposeAllChildren();

        foreach (var action in actions)
        {
            var id = action.Id;
            var button = new Button
            {
                Text = Loc.GetString(action.Text),
                Disabled = action.Disabled,
                MinWidth = 130,
                MinHeight = 30,
            };

            if (!string.IsNullOrEmpty(action.Tooltip))
                button.ToolTip = Loc.GetString(action.Tooltip);

            button.OnPressed += _ => ActionPressed?.Invoke(id);
            CompletionActionButtons.AddChild(button);
        }
    }
}
