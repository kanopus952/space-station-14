using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Timing;

namespace Content.Client._Sunrise.Tutorial.TutorialWindow;

[GenerateTypedNameReferences]
public sealed partial class TutorialPromptPopup : Control
{
    private float _timer;

    public event Action? OnStartPressed;
    public event Action? OnSkipPressed;

    public float Timer
    {
        get => _timer;
        set
        {
            _timer = MathF.Max(0f, value);
            SkipButton.Disabled = _timer > 0f;
            UpdateWaitLabel();
        }
    }

    public TutorialPromptPopup()
    {
        RobustXamlLoader.Load(this);

        StartButton.OnPressed += OnStartButtonPressed;
        SkipButton.OnPressed += OnSkipButtonPressed;
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        if (!SkipButton.Disabled)
            return;

        Timer -= args.DeltaSeconds;
    }

    private void OnStartButtonPressed(BaseButton.ButtonEventArgs args)
    {
        OnStartPressed?.Invoke();
    }

    private void OnSkipButtonPressed(BaseButton.ButtonEventArgs args)
    {
        if (SkipButton.Disabled)
            return;

        OnSkipPressed?.Invoke();
    }

    private void UpdateWaitLabel()
    {
        WaitLabel.Text = SkipButton.Disabled
            ? Loc.GetString("tutorial-prompt-wait", ("time", MathF.Ceiling(_timer)))
            : Loc.GetString("tutorial-prompt-skip-available");
    }
}
