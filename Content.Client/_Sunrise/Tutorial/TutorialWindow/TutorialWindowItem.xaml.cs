using System;
using System.Numerics;
using Content.Client.Message;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;
using SixLabors.ImageSharp.Processing;

namespace Content.Client._Sunrise.Tutorial.TutorialWindow;

[GenerateTypedNameReferences]
public sealed partial class TutorialWindowItem : Control
{
    private Action? _onStartPressed;
    private Color _backgroundColor = new(30, 30, 34);

    public TutorialWindowItem(TutorialWindowItemConfig config)
    {
        RobustXamlLoader.Load(this);

        MainPanel.PanelOverride = new StyleBoxFlat(_backgroundColor);

        StartButton.OnPressed += _ => _onStartPressed?.Invoke();
        ApplyConfig(config);
    }

    public void ApplyConfig(TutorialWindowItemConfig config)
    {
        _onStartPressed = config.OnStartPressed;

        TitleLabel.Text = Loc.GetString(config.TitleText);
        PreviewTexture.Visible = config.Texture != null;
        PreviewTexture.Texture = null;
        PreviewTexture.CropFocus = config.CropFocus;
        if (config.Texture != null)
            PreviewTexture.TexturePath = config.Texture.Value.ToString();

        DescriptionLabel.SetMarkup(Loc.GetString(config.DescriptionText));
        StatusLabel.SetMarkup(config.StatusText);
        TimeLabel.SetMarkup(config.TimeText);

        StartButton.Text = config.StartText;
        StartButton.Disabled = config.StartDisabled;
    }
}

public sealed record TutorialWindowItemConfig
{
    public string TitleText { get; init; } = string.Empty;
    public ResPath? Texture { get; init; }
    public Vector2 CropFocus { get; init; } = new(0.5f, 0.5f);
    public string DescriptionText { get; init; } = string.Empty;
    public string StatusText { get; init; } = string.Empty;
    public string TimeText { get; init; } = string.Empty;
    public string StartText { get; init; } = string.Empty;
    public bool StartDisabled { get; init; }
    public Action? OnStartPressed { get; init; }
}
