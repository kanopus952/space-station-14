using System;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;

namespace Content.Client._Sunrise.SponsorInventory;

/// <summary>
/// Rich tooltip container for InventoryWindow palette cards.
/// </summary>
[GenerateTypedNameReferences]
internal sealed partial class InventoryPaletteItemTooltip : PanelContainer
{
    public InventoryPaletteItemTooltip(FormattedMessage tooltip, Type[] tooltipTags)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        // InventoryWindow composes the FormattedMessage; this control only applies the restricted tag set.
        TooltipText.SetMessage(tooltip, tooltipTags);
    }
}
